export function convertToCSV(dataset: SampleDataset): string {
  const headers = ["شناسه کاربر", "زمان ثبت", "پیام کاربر", "یادداشت پشتیبانی"];
  const rows = dataset.chats.map(chat => [
    chat.userId,
    chat.timestamp,
    `"${chat.message.replace(/"/g, '""')}"`,
    `"${chat.supportNotes.replace(/"/g, '""')}"`
  ]);
  
  return [headers.join(","), ...rows.map(e => e.join(","))].join("\n");
}
